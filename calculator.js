// Define the numbers and the operation here
let firstNum = 18;
let secondNum = 6;
let mathOp = "/";
// Write if-statements here
if (mathOp == "+") {
    console.log(firstNum + secondNum);
} else if (mathOp == "-") {
    console.log(firstNum - secondNum);
} else if (mathOp == "*") {
    console.log(firstNum * secondNum);
} else if (mathOp == "/") {
    console.log(firstNum / secondNum);
}